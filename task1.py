# Створено змінну filename для збереження імені нового файлу, введеного користувачем
filename = input("Введіть ім'я нового файлу (наприклад, 'file.txt'): ")

# Відкриваємо файл у режимі запису ('w') за допомогою менеджера контексту
# Створено змінну file для роботи з відкритим файлом
with open(filename, 'w', encoding='utf-8') as file:
    print("Введіть текст (для завершення введіть пустий рядок):")
    while True:
        # Створено змінну line для збереження рядка, введеного користувачем
        line = input()
        if line == "":
            break  # Вихід з циклу при пустому рядку
        # Записуємо рядок у файл з переходом на новий рядок
        file.write(line + "\n")

# Відкриваємо той самий файл у режимі читання ('r')
with open(filename, 'r', encoding='utf-8') as file:
    # Створено змінну content для збереження всього вмісту файлу
    content = file.read()
    print("\nВміст файлу:")
    print(content)
